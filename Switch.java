
class Switch {
	public static void main ()
	{
		int age;
		age=3;
		switch (age){
			case 1:
				System.out.println("You can crawl");
				break;
		     case 2:
				System.out.println("You can talk");
				break;
               case 3:
				System.out.println("You can get in trouble");
				break;
				default:
					System.out.println("Wrong input");
					break;

					
		}
	}
}
